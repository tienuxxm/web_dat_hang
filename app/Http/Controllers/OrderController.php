    public function combine(Request $request)
    {
        $user = JWTAuth::user();

        if (!$user->isInDepartment('CUNG_UNG')) {
            return response()->json(['message' => 'Kh√¥ng c√≥ quy·ªÅn g·ªôp ƒë∆°n'], 403);
        }

        $orderIds = $request->input('order_ids', []);
        if (empty($orderIds)) {
            return response()->json(['message' => 'Ch∆∞a ch·ªçn ƒë∆°n h√†ng n√†o'], 422);
        }
        Order::whereIn('id', $orderIds)->update(['merged' => true]);


        $orders = Order::with('items.product')
            ->whereIn('id', $orderIds)
            ->where('status', 'fulfilled')
            ->where('payment_status', 'paid')
            ->get();

        if ($orders->count() < 1) {
            return response()->json(['message' => 'Kh√¥ng c√≥ ƒë∆°n h·ª£p l·ªá ƒë·ªÉ g·ªôp'], 422);
        }

        // B·∫Øt ƒë·∫ßu gom s·∫£n ph·∫©m
        $itemsByProduct = collect();

        foreach ($orders as $order) {
        foreach ($order->items as $item) {
                $pid = $item->product_id;

                $existing = $itemsByProduct->get($pid);

                if ($existing) {
                    $existing['quantity'] += $item->quantity;
                } else {
                    $existing = [
                        'product'  => $item->product,
                        'quantity' => $item->quantity,
                    ];
                }

                $itemsByProduct->put($pid, $existing);
            }
        }


        DB::beginTransaction();
        try {
            $subtotal = 0;
            foreach ($itemsByProduct as $row) {
                $subtotal += $row['quantity'] * $row['product']->price;
            }

            $tax = round($subtotal * 0.08, 2);
            $shipping = 0;
            $total = $subtotal + $tax + $shipping;
            // T·ª± sinh order_number cho ƒë∆°n g·ªôp
            $prefix = 'XX'; // Ho·∫∑c l·∫•y t·ª´ prefix chung (n·∫øu c√≥)
            $timestamp = now('Asia/Ho_Chi_Minh')->format('ymdHis');
            $random = strtoupper(Str::random(4));
            $orderNumber = "{$prefix}-{$timestamp}-{$random}";


            $newOrder = Order::create([
                'order_number'       => $orderNumber, 

                'user_id'         => $user->id,
                'order_date'      => now(),
                'shipping_address'=> 'G·ªôp t·ª´ nhi·ªÅu ƒë∆°n',
                'supplier_name'   => 'N/A',
                'estimated_delivery' => now()->addDays(7),
                'status'          => 'draft',
                'payment_status'  => 'pending',
                'subtotal'        => $subtotal,
                'tax'             => $tax,
                'shipping'        => $shipping,
                'total_amount'    => $total,
                'notes'           => 'G·ªôp t·ª´ ƒë∆°n: ' . implode(', ', $orders->pluck('order_number')->toArray()),
            ]);

            foreach ($itemsByProduct as $row) {
                $product = $row['product'];
                $qty = $row['quantity'];

                OrderItem::create([
                    'order_id'     => $newOrder->id,
                    'product_id'   => $product->id,
                    'quantity'     => $qty,
                    'unit_price'   => $product->price,
                    'product_name' => $product->name,
                    'line_total'   => $qty * $product->price,
                ]);
                $product->increment('quantity', $qty); // ‚úÖ c·ªông v√†o t·ªìn kho

            }

            DB::commit();

            return response()->json([
                'message' => 'ƒê√£ g·ªôp ƒë∆°n th√†nh c√¥ng',
                'order'   => $newOrder->load('items.product'),
            ]);

        } catch (\Throwable $e) {
            DB::rollBack();
            return response()->json(['message' => 'G·ªôp th·∫•t b·∫°i', 'error' => $e->getMessage()], 500);
        }
    }

    public function mergedByMonth()
    {
        $orders = Order::with('items.product')
            ->where('status', 'fulfilled')
            ->where('payment_status', 'paid')
            ->where('merged', true) // c·ªôt merged = true
            ->get();

        $grouped = $orders->groupBy(function ($order) {
            return Carbon::parse($order->order_date)->format('m/Y');
        });

        $result = [];

        foreach ($grouped as $month => $ordersInMonth) {
            $items = [];

            foreach ($ordersInMonth as $order) {
                foreach ($order->items as $item) {
                    $key = $item->product_id;

                    if (!isset($items[$key])) {
                        $items[$key] = [
                            'product_id' => $item->product_id,
                            'product_name' => $item->product_name,
                            'price' => $item->unit_price,
                            'total_quantity' => 0
                        ];
                    }

                    $items[$key]['total_quantity'] += $item->quantity;
                }
            }

            $result[] = [
                'month' => $month,
                'items' => array_values($items)
            ];
        }

        return response()->json($result);
    }
    public function search(Request $request)
    {
        $q = $request->query('q', '');

        $orders = Order::with(['items.product.category'])
            ->where('order_number', 'like', "%$q%")
            ->orWhere('supplier_name', 'like', "%$q%")
            ->orWhereHas('items', function ($query) use ($q) {
                $query->where('product_name', 'like', "%$q%");
            })
            ->orWhereHas('items.product', function ($query) use ($q) {
                $query->where('barcode', 'like', "%$q%")
                    ->orWhere('color', 'like', "%$q%");
            })
            ->orWhereHas('items.product.category', function ($query) use ($q) {
                $query->where('name', 'like', "%$q%");
            })
            ->get();

        return response()->json($orders);
    }
    public function importMultipleOrders(Request $request)
    {
        try {
            $user = JWTAuth::user();
            $this->authorize('create', Order::class);

            if (!$request->hasFile('file')) {
                return response()->json(['message' => 'Vui l√≤ng ch·ªçn file Excel ƒë·ªÉ import.'], 400);
            }

            $file = $request->file('file');
            $spreadsheet = IOFactory::load($file->getPathname());
            $sheet = $spreadsheet->getActiveSheet();
            $rows = $sheet->toArray(null, true, true, true);

            // B·ªè qua d√≤ng ti√™u ƒë·ªÅ
            array_shift($rows);

            if (count($rows) === 0) {
                return response()->json(['message' => 'File Excel kh√¥ng c√≥ d·ªØ li·ªáu.'], 422);
            }

            // Gom nh√≥m s·∫£n ph·∫©m theo (supplier_name + address)
            $groupedOrders = [];

            foreach ($rows as $index => $row) {
                $barcode  = trim($row['A'] ?? '');
                $color    = trim($row['B'] ?? '');
                $quantity = intval($row['C'] ?? 0);
                $address  = trim($row['D'] ?? '');
                $supplierName = trim($row['E'] ?? '');

                if (!$barcode || !$color || $quantity <= 0 || !$address || !$supplierName) {
                    return response()->json([
                        'message' => "D√≤ng " . ($index + 2) . " thi·∫øu th√¥ng tin c·∫ßn thi·∫øt."
                    ], 422);
                }

                $product = Product::where('barcode', $barcode)->where('color', $color)->first();
                if (!$product) {
                    return response()->json([
                        'message' => "Kh√¥ng t√¨m th·∫•y s·∫£n ph·∫©m {$barcode} - {$color} (D√≤ng " . ($index + 2) . ")"
                    ], 404);
                }

                $key = $supplierName . '___' . $address;
                $groupedOrders[$key][] = [
                    'product' => $product,
                    'quantity' => $quantity
                ];
            }

            $results = [];

            foreach ($groupedOrders as $groupKey => $items) {
                [$supplierName, $address] = explode('___', $groupKey);

                $subtotal = 0;
                $categoryId = null;
                $orderItems = [];

                foreach ($items as $item) {
                    $product = $item['product'];
                    $quantity = $item['quantity'];

                    // G√°n danh m·ª•c ƒë·∫ßu ti√™n
                    if (is_null($categoryId)) {
                        $categoryId = $product->category_id;

                        if ($user->role->name_role === 'nhan_vien_chinh_thuc') {
                            $allowed = $user->categories()->pluck('categories.id')->toArray();
                            if (!in_array($categoryId, $allowed)) {
                                return response()->json([
                                    'message' => "B·∫°n kh√¥ng c√≥ quy·ªÅn t·∫°o ƒë∆°n v·ªõi danh m·ª•c s·∫£n ph·∫©m n√†y: {$product->name}"
                                ], 403);
                            }
                        }
                    }

                    // Ki·ªÉm tra c√πng danh m·ª•c
                    if ($product->category_id !== $categoryId) {
                        return response()->json([
                            'message' => "T·∫•t c·∫£ s·∫£n ph·∫©m trong m·ªói ƒë∆°n ph·∫£i thu·ªôc c√πng danh m·ª•c. Danh m·ª•c kh√°c nhau: {$product->name}"
                        ], 422);
                    }

                    $subtotal += $quantity * $product->price;

                    $orderItems[] = [
                        'product_id' => $product->id,
                        'quantity'   => $quantity,
                        'unit_price' => $product->price
                    ];
                }

                $tax = round($subtotal * 0.08, 2);
                $shipping = 0;
                $total = $subtotal + $tax + $shipping;

                // T·∫°o m√£ ƒë∆°n
                $prefix = DB::table('categories')->where('id', $categoryId)->value('prefix') ?? 'XX';
                $timestamp = now('Asia/Ho_Chi_Minh')->format('ymdHis');
                $random = strtoupper(Str::random(4));
                $orderNumber = "{$prefix}-{$timestamp}-{$random}";

                // T·∫°o ƒë∆°n h√†ng
                $order = Order::create([
                    'order_number'     => $orderNumber,
                    'user_id'          => $user->id,
                    'status'           => 'draft',
                    'subtotal'         => $subtotal,
                    'tax'              => $tax,
                    'shipping'         => $shipping,
                    'total_amount'     => $total,
                    'supplier_name'    => $supplierName,
                    'shipping_address' => $address,
                    'payment_status'   => 'pending',
                    'order_date'       => now(),
                ]);

                foreach ($orderItems as $item) {
                    $order->items()->create($item);
                }

                $results[] = [
                    'order_number' => $order->order_number,
                    'total_amount' => $total
                ];
            }

            return response()->json([
                'message' => 'Import th√†nh c√¥ng',
                'orders'  => $results
            ]);

        } catch (\Exception $e) {
            return response()->json([
                'message' => 'L·ªói khi import Excel',
                'error' => $e->getMessage(),
            ], 500);
        }
    }

