    public function store(Request $request)
    {
        $user = JWTAuth::user();
        $this->authorize('create', Order::class); // ‚ë† Ki·ªÉm tra quy·ªÅn t·ªïng

        // ‚ë° Validate ƒë·∫ßu v√†o
        $validated = $request->validate([
            'orderDate'         => 'required|date',
            'shippingAddress'   => 'required|string',
            'supplier_name'     => 'required|string|max:255',
            'items'             => 'required|array|min:1',
            'items.*.productCode' => ['required', 'string', Rule::exists('products', 'code')],
            'items.*.quantity'  => 'required|integer|min:1',
            'status'            => ['nullable', Rule::in(Order::STATUSES)],
            'payment_status'    => ['nullable', Rule::in(Order::PAYMENT_STATUSES)],
            'estimatedDelivery' => 'nullable',
            'shipping'          => 'required|numeric|min:0',
            'notes'             => 'nullable|string',
        ]);

        // ‚ë¢ X√°c ƒë·ªãnh category_id c·ªßa s·∫£n ph·∫©m ƒë·∫ßu ti√™n
        $firstProduct    = Product::where('code', $request->items[0]['productCode'])->firstOrFail();
        $orderCategoryId = $firstProduct->category_id;

        // ‚ë£ Ki·ªÉm tra quy·ªÅn theo category n·∫øu l√† nh√¢n vi√™n
        if ($user->role->name_role === 'nhan_vien_chinh_thuc') {
            $allowed = $user->categories()->pluck('categories.id')->toArray();
            if (!in_array($orderCategoryId, $allowed)) {
                return response()->json(['message' => 'B·∫°n kh√¥ng ƒë∆∞·ª£c ph√©p t·∫°o ƒë∆°n v·ªõi danh m·ª•c n√†y.'], 403);
            }
        }

        // ‚ë§ Ki·ªÉm tra c√°c s·∫£n ph·∫©m ƒë·ªÅu c√πng category + t·ªìn kho
        $subtotal = 0;
        foreach ($request->items as $item) {
            $product = Product::where('code', $item['productCode'])->firstOrFail();

            // 5.1 Ki·ªÉm tra c√πng danh m·ª•c
            if ($product->category_id !== $orderCategoryId) {
                return response()->json([
                    'message' => 'T·∫•t c·∫£ s·∫£n ph·∫©m trong ƒë∆°n ph·∫£i thu·ªôc c√πng m·ªôt danh m·ª•c.'
                ], 422);
            }

            // 5.2 T√≠nh t·ªïng
            $subtotal += $item['quantity'] * $product->price;
        }

        // ‚ë• T√≠nh to√°n t·ªïng ti·ªÅn
        $tax      = round($subtotal * 0.08, 2);
        $shipping = $request->shipping;
        $total    = $subtotal + $tax + $shipping;

        $prefix = DB::table('categories')->where('id', $orderCategoryId)->value('prefix') ?? 'XX';
            $timestamp = now('Asia/Ho_Chi_Minh')->format('ymdHis');
            $random    = strtoupper(Str::random(4));
            $orderNumber = "{$prefix}-{$timestamp}-{$random}";

        // ‚ë¶ T·∫°o ƒë∆°n h√†ng v√† item trong transaction
        DB::beginTransaction();
        try {
            // ‚ë¶.1 L·∫•y prefix theo category
            

            $order = Order::create([
                'order_number'       => $orderNumber,
                'total_amount'       => $total,
                'status'             => $request->status ?? 'draft',
                'payment_status'     => $request->payment_status ?? 'pending',
                'user_id'            => $user->id,
                'shipping_address'   => $request->shippingAddress,
                'supplier_name'      => $request->supplier_name,
                'order_date'         => $request->orderDate,
                'estimated_delivery' => $request->estimatedDelivery,
                'notes'              => $request->notes,
                'subtotal'           => $subtotal,
                'tax'                => $tax,
                'shipping'           => $shipping,
            ]);

            foreach ($request->items as $item) {
                $product = Product::where('code', $item['productCode'])->firstOrFail();

                OrderItem::create([
                    'order_id'     => $order->id,
                    'product_id'   => $product->id,
                    'quantity'     => $item['quantity'],
                    'unit_price'   => $product->price,
                    'product_name' => $product->name,
                    'barcode'     => $product->barcode, 
                    'color'       => $product->color, 
                    'line_total'   => $product->price * $item['quantity'],
                ]);
            }

            DB::commit();
            return response()->json([
                'message' => 'Order created successfully.',
                'order'   => $order->load('items.product'),
            ], 201);
        } catch (\Throwable $e) {
            DB::rollBack();
            return response()->json([
                'message' => 'Failed to create order',
                'error'   => $e->getMessage(),
            ], 500);
        }
    }


    public function update(Request $request, Order $order)
    {
        $user = JWTAuth::user();
        $this->authorize('update', $order);

        $statusBefore = $order->status;
        $statusAfter  = $request->get('status', $statusBefore);

        // üéØ VALIDATE chuy·ªÉn tr·∫°ng th√°i ƒë√∫ng theo role
        if ($user->isInDepartment('KINH_DOANH') && $statusAfter !== $statusBefore) {
        if (!in_array($statusBefore, ['draft']) || $statusAfter !== 'pending') {
            return response()->json(['message' => 'KD ch·ªâ ƒë∆∞·ª£c chuy·ªÉn t·ª´ draft sang pending.'], 403);
            }
        }

        if ($user->isInDepartment('CUNG_UNG') && $statusAfter !== $statusBefore) {
            if ($statusBefore === 'pending' && $statusAfter === 'draft') {
                // OK
            } elseif( in_array($statusBefore, ['pending', 'rejected'])  && $statusAfter === 'approved') {
                // OK
            } else {
                return response()->json(['message' => 'CU ch·ªâ ƒë∆∞·ª£c chuy·ªÉn pending ‚Üí draft ho·∫∑c pending ‚Üí approved v√† rejected ‚Üí approved.'], 403);
            }
        }

        if ($user->isRole('giam_doc') && $statusAfter !== $statusBefore) {
            if ($statusBefore !== 'approved' || !in_array($statusAfter, ['fulfilled', 'rejected'])) {
                return response()->json(['message' => 'GD ch·ªâ ƒë∆∞·ª£c duy·ªát t·ª´ approved ‚Üí fulfilled / rejected.'], 403);
            }
        }


        /* 2. Validate */
        $isPatch = $request->isMethod('patch');
        $req     = $isPatch ? 'sometimes' : 'required';

        $rules = [
            'orderDate'           => $req . '|date',
            'shippingAddress'     => $req . '|string',
            'supplier_name'       => $req . '|string|max:255',
            'items'               => $req . '|array|min:1',
            // üîë so s√°nh & validate theo productCode
            'items.*.productCode' => [$req, 'string', Rule::exists('products', 'code')],
            'items.*.quantity'    => $req . '|integer|min:1',
            'status'              => [$req, Rule::in(Order::STATUSES)],
            'payment_status'       => [$req, Rule::in(Order::PAYMENT_STATUSES)],
            'estimated_delivery'   => 'nullable|date|after_or_equal:orderDate',
            'shipping'            => 'sometimes|numeric|min:0',
            'notes'               => 'nullable|sometimes|string'
        ];
        $validated = $request->validate($rules);

        /* 3. Chu·∫©n b·ªã d·ªØ li·ªáu m·ªõi */
        $hasItems = $request->has('items');
        $canModifyItemsFully = false;

        // üéØ Ki·ªÉm tra quy·ªÅn s·ª≠a s·∫£n ph·∫©m
        if ($hasItems) {
            if ($user->isInDepartment('KINH_DOANH')) {
                if ($order->status !== 'draft') {
                    return response()->json(['message' => 'KINH DOANH ch·ªâ ƒë∆∞·ª£c s·ª≠a ƒë∆°n ·ªü tr·∫°ng th√°i draft.'], 403);
                }
                $canModifyItemsFully = true; // ƒê∆∞·ª£c th√™m / xo√° / s·ª≠a s·∫£n ph·∫©m
            } else {
                // V·ªõi CUNG ·ª®NG v√† GI√ÅM ƒê·ªêC ‚Üí ch·ªâ ƒë∆∞·ª£c s·ª≠a s·ªë l∆∞·ª£ng s·∫£n ph·∫©m
                $submittedCodes = collect($request->items)->pluck('productCode')->sort()->values();
                $existingCodes  = $order->items()->with('product')->get()
                                    ->pluck('product.code')->sort()->values();

                if ($submittedCodes->count() !== $existingCodes->count() ||
                    !$submittedCodes->every(fn($code, $idx) => $code === $existingCodes[$idx])) {
                    return response()->json(['message' => 'B·∫°n kh√¥ng ƒë∆∞·ª£c th√™m/xo√° ho·∫∑c thay ƒë·ªïi s·∫£n ph·∫©m. Ch·ªâ ƒë∆∞·ª£c ch·ªânh s·ª≠a s·ªë l∆∞·ª£ng.'], 403);
                }
            }
        }

        $subtotal   = 0;
        $newByCode  = collect();   // key = productCode

        if ($hasItems) {
             $firstProduct    = Product::where('code', $request->items[0]['productCode'])->firstOrFail();
            $orderCategoryId = $firstProduct->category_id;

            if (!$user->isManager()) {
                $allowed = $user->categories()->pluck('categories.id')->toArray();
            if (!$orderCategoryId || !in_array($orderCategoryId, $allowed)) {
                    return response()->json(['message' => 'B·∫°n kh√¥ng c√≥ quy·ªÅn v·ªõi danh m·ª•c n√†y.'], 403);
                }
            }

            foreach ($request->items as $it) {
                $product = Product::where('code', $it['productCode'])->firstOrFail();

                if ($product->category_id !== $orderCategoryId) {
                    return response()->json(['message' => 'T·∫•t c·∫£ s·∫£n ph·∫©m ph·∫£i c√πng danh m·ª•c.'], 422);
                }

                $qty = $it['quantity'];
                $subtotal += $qty * $product->price;

                if ($canModifyItemsFully) {
                    // ‚úÖ Tr∆∞·ªùng h·ª£p KINH DOANH: update ƒë·∫ßy ƒë·ªß
                    $newByCode->put($product->code, [
                        'product'  => $product,
                        'quantity' => $qty
                    ]);
                } else {
                    // ‚úÖ Tr∆∞·ªùng h·ª£p CUNG ·ª®NG / GI√ÅM ƒê·ªêC: ch·ªâ update s·ªë l∆∞·ª£ng
                    $order->items()->where('product_id', $product->id)->update([
                        'quantity'   => $qty,
                        'line_total' => $product->price * $qty,
                    ]);
                }
            }

        }

        /* 4. Giao d·ªãch */
        DB::beginTransaction();
        try {
            /* 4.1 C·∫≠p nh·∫≠t header ƒë∆°n */
            $order->fill($validated);

            // Cho ph√©p c·∫≠p nh·∫≠t ng√†y giao h√†ng: ch·ªâ khi CUNG ·ª®NG duy·ªát t·ª´ pending ‚Üí approved
            if ($user->isInDepartment('CUNG_UNG') && $statusBefore === 'pending' && $statusAfter === 'approved') {
                if (!$request->filled('estimated_delivery')) {
                    return response()->json(['message' => 'C·∫ßn ch·ªçn ng√†y giao h√†ng khi duy·ªát ƒë∆°n.'], 422);
                }
                if ($request->payment_status !== 'paid') {
                    return response()->json(['message' => 'ƒê∆°n h√†ng ph·∫£i ƒë∆∞·ª£c thanh to√°n tr∆∞·ªõc khi duy·ªát.'], 422);
                }
                $order->estimated_delivery = $request->estimated_delivery;
            } else {
                // Tr∆∞·ªùng h·ª£p kh√°c kh√¥ng ƒë∆∞·ª£c s·ª≠a ng√†y giao h√†ng
                unset($validated['estimated_delivery']);
            }


            if ($hasItems) {
                $shipping = $request->get('shipping', $order->shipping ?? 0);
                $tax      = round($subtotal * 0.08, 2);
                $total    = $subtotal + $tax + $shipping;

                $order->subtotal     = $subtotal;
                $order->tax          = $tax;
                $order->shipping     = $shipping;
                $order->total_amount = $total;
            }
            $order->save();

            /* 4.2 L·∫•y danh s√°ch item c≈© theo productCode */
            $oldItems = $order->items()
                            ->with('product:id,code,name,price')
                            ->get()
                            ->keyBy(fn($item) => $item->product->code);   // key = productCode

                        /* a. Th√™m m·ªõi / c·∫≠p nh·∫≠t */
            $keptProductIds = [];          // ‚≠ê s·∫Ω ch·ª©a ID ƒë√£ x·ª≠ l√Ω

            foreach ($newByCode as $code => $row) {
                $product = $row['product'];
                $qty     = $row['quantity'];

                $order->items()->updateOrCreate(
                    ['product_id' => $product->id],
                    [
                        'quantity'     => $qty,
                        'unit_price'   => $product->price,
                        'product_name' => $product->name,
                        'barcode'     => $product->barcode,
                        'color'       => $product->color,
                        'line_total'   => $product->price * $qty,
                    ]
                );

                $keptProductIds[] = $product->id;     // ‚≠ê thu th·∫≠p ID
            }

            /* b. Xo√° nh·ªØng product_id kh√¥ng c√≤n ƒë∆∞·ª£c gi·ªØ l·∫°i */
            if ($canModifyItemsFully) {
                $order->items()->whereNotIn('product_id', $keptProductIds)->delete();
            }

            DB::commit();
            // üîî G·ª≠i th√¥ng b√°o cho b·ªô ph·∫≠n c√≥ li√™n quan
            $relatedUsers = User::where('id', '!=', $user->id)->get();

            foreach ($relatedUsers as $u) {
                Notification::create([
                    'order_id'    => $order->id,
                    'sender_id'   => $user->id,
                    'user_id' => $u->id,
                    'type'        => 'order_updated',
                    'message'     => "ƒê∆°n h√†ng #{$order->order_number} v·ª´a ƒë∆∞·ª£c ch·ªânh s·ª≠a b·ªüi {$user->name}",
                    'expires_at' => now()->addHours(1),

                ]);
            }

            return response()->json([
                'message' => 'Order updated successfully.',
                'order'   => $order->load('items')
            ]);

        } catch (\Throwable $e) {
            DB::rollBack();
            return response()->json([
                'message' => 'Update failed',
                'error'   => $e->getMessage()
            ], 500);
        }
    }
    public function index(Request $request)
    {
        $user = auth()->user();
        $this->authorize('viewAny', Order::class); // ƒê√£ ƒë∆∞·ª£c s·ª≠a ƒë√∫ng

        $query = Order::with('items.product')
            ->where('merged', false) ;// Ch·ªâ l·∫•y ƒë∆°n ch∆∞a g·ªôp

        // X√°c ƒë·ªãnh tr·∫°ng th√°i ph√π h·ª£p v·ªõi t·ª´ng vai tr√≤/ph√≤ng
        if ($user->isRole('giam_doc')) {
            $query->whereIn('status', ['approved', 'rejected', 'fulfilled']);
        } elseif ($user->isInDepartment('KINH_DOANH') && ($user->isManager() || $user->isEmployee())) {
            $query->whereIn('status', ['draft', 'pending']);
        } elseif ($user->isInDepartment('CUNG_UNG') && ($user->isManager() || $user->isEmployee())) {
            $query->whereIn('status', ['pending', 'rejected', 'fulfilled']);
        } else {
            return response()->json(['message' => 'B·∫°n kh√¥ng c√≥ quy·ªÅn xem ƒë∆°n h√†ng'], 403);
        }

        $orders = $query->get();

        $filtered = $orders->filter(function ($order) use ($user) {
            return Gate::forUser($user)->allows('view', $order); // ki·ªÉm tra theo category n·∫øu l√† nh√¢n vi√™n
        });

        // ‚úÖ Ph√¢n trang th·ªß c√¥ng
        $page = $request->input('page', 1);
        $perPage = 10;
        $paginated = $filtered->values()->forPage($page, $perPage);

        return response()->json([
            'data' => $paginated,
            'total' => $filtered->count(),
            'current_page' => $page,
            'last_page' => ceil($filtered->count() / $perPage),
        ]);
    }






